/**
 * Pre-build script: reads KaTeX CSS from node_modules and writes
 * src/katex-css-data.ts so the CSS is baked into the compiled bundle.
 *
 * This is the correct solution for consumers that bundle @changedown/preview
 * with esbuild (CJS or ESM) where import.meta.url / require.resolve at runtime
 * may not resolve paths inside node_modules.
 *
 * Run: node scripts/generate-katex-data.mjs
 */

import { readFileSync, writeFileSync } from 'fs';
import { createRequire } from 'module';
import { fileURLToPath } from 'url';
import { dirname, join } from 'path';

const __dirname = dirname(fileURLToPath(import.meta.url));
const require = createRequire(import.meta.url);

const katexCssPath = require.resolve('katex/dist/katex.min.css');
const katexCss = readFileSync(katexCssPath, 'utf8');

const outputPath = join(__dirname, '..', 'src', 'katex-css-data.ts');

const content = `/**
 * AUTO-GENERATED — do not edit manually.
 * Generated by scripts/generate-katex-data.mjs at build time.
 * Contains KaTeX CSS inlined so it works in all bundle contexts
 * (Node.js scripts, esbuild CJS bundles, Vite ESM bundles).
 */

// eslint-disable-next-line @typescript-eslint/no-unused-vars
export const KATEX_CSS: string = ${JSON.stringify(katexCss)};
`;

writeFileSync(outputPath, content, 'utf8');
console.log(`Generated ${outputPath} (${katexCss.length} bytes of KaTeX CSS)`);
